var catNames = ["Ash", "Spooky", "Cutie", "Sky"];
// .push() method- adds one or more elements to the end of the array.
catNames.push("Sammy");
console.log(catNames);

var kidAges = [13, 12, 10, 1];
//.shift() method- removes the first element from an array. 
kidAges.shift();
console.log(kidAges);

var expressions = [4<3, 7 === 7, 17 < 100, 14>10];
// .pop() method- removes the last element from an array.
expressions.pop();
console.log(expressions);

